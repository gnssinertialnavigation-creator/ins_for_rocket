void get_rocket_body_omega( double t, double C_b2e[3][3],
                            double gyro_body[3] )
{
  double ten_times_t = t * 10;

  if( ( t >= 0 ) && ( ten_times_t < IMU_GY_INPUT_BUFF_SIZE ) )
  {
    gyro_body[0] = 0;
    gyro_body[1] = ( DEG_2_RAD * imu_gy_deg_s[ (int)( ten_times_t ) ] );
    gyro_body[2] = 0;

    return;
  }

  double omega_ecef[3] = { 0, 0, OMEGA_EARTH };
  double C_e2b[3][3];
  matrix_t( 3, 3, C_b2e, C_e2b );
  matrix_vector_product( 3, 3, C_e2b, omega_ecef, gyro_body );
}
