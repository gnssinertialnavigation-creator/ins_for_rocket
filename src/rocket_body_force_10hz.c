void get_rocket_body_force( double t, double C_b2e[3][3],
                            double p_ecef[3], double f_body[3] )
{
  double ten_times_t = t * 10;

  if( ( t >= 0 ) && ( ten_times_t < IMU_FX_INPUT_BUFF_SIZE ) )
  {
    f_body[0] = 0;
    f_body[1] = 0;
    f_body[2] = ( G_2_N * imu_fx_g[ (int)( ten_times_t ) ] );

    return;
  }

  double g_ecef[3];
  simple_gravity_model( p_ecef, g_ecef );

  double C_e2b[3][3];
  matrix_t( 3, 3, C_b2e, C_e2b );
  matrix_vector_product( 3, 3, C_e2b, g_ecef, f_body );
  f_body[0] *= -1;
  f_body[1] *= -1;
  f_body[2] *= -1;
}
